        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Edit(int id, [Bind("CodTicket,Nreserva,Usuario,CodEmpresa,Fecha,Estado")] data.Tickets tickets)
        {
            if (id != tickets.CodTicket)
            {
                return NotFound();
